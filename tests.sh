#!/bin/sh

set -e

./bootstrap_ca.sh

basedir=`realpath testdata`
certalator_dir=`realpath .`
uid=`whoami`

cleanup()
{
	kill `cat $basedir/authority1/mdrd.pid`
}

trap cleanup TERM INT EXIT

#
# Authority setup
#

cat << EOF > $basedir/authority1/certalator.conf
enable_coredumps = true
authority_fqdn = "localhost"
authority_port = 9791
certdb_path = "$basedir/authority1/certdb.sqlite"
ca_file = "$basedir/ca/root.pem"
crl_path = "$basedir/authority1/crl_store"
cert_file = "$basedir/authority1/cert.pem"
key_file = "$basedir/authority1/key.pem"
lock_file = "$basedir/authority1/coordinator.lock"
coordinator_socket_path = "$basedir/authority1/coordinator.sock"
serial_file = "$basedir/authority1/serial"
cert_org = "Example"
cert_email = "cert@example.com"
min_serial = "0x1000000"
max_serial = "0x1ffffff"
EOF

cat << EOF > $basedir/authority1/mdrd.conf
uid = $uid
gid = $uid
enable_coredumps = true
ca_file = "$basedir/ca/root.pem"
crl_path = "$basedir/authority1/crl_store"
cert_file = "$basedir/proxy1/cert.pem"
key_file = "$basedir/proxy1/key.pem"
require_client_cert = false

port = 9791
listen_backlog = 1024
prefork = 4
pid_file = "$basedir/authority1/mdrd.pid"
counters_sock = "$basedir/authority1/counters.sock"

max_clients = 10000
max_conn_per_ip = 25
use_rcv_lowat = true
so_debug = false
rcvbuf = 0
sndbuf = 0
socket_timeout_min = 3
socket_timeout_max = 300
max_payload_size = 16384
max_cert_size = 4096
dbg_delay_to_backend_seconds = 5

allowed_mdr_domains = [ 0x00000002 ]

backend_argv = [
	"$(realpath certalator)"
	"-config"
	"$basedir/authority1/certalator.conf"
	"mdrd-backend"
]
backend_uid = $uid
backend_gid = $uid
backend_promises = "stdio rpath"
backend_unveils = [
	"x=$(realpath certalator)"
	"r=$basedir/ca/root.pem"
	"r=$basedir/ca/root.crl"
	"r=$basedir/authority1/key.pem"
	"r=$basedir/authority1/cert.pem"
	"r=$basedir/authority1/coordinator.lock"
	"rw=$basedir/authority1/coordinator.sock"
	"rw=$basedir/authority1/serial"
	"rw=$basedir/authority1/certdb.sqlite"
]
EOF

ulimit -c unlimited
../mdr/mdrd -c $basedir/authority1/mdrd.conf

for i in 1 2 3 4 5; do
	nc -vz localhost 9791 >/dev/null 2>&1 && break
	sleep 1
done

#
# Agent setup
#

./certalator -config $basedir/authority1/certalator.conf \
	bootstrap-setup -cn `hostname` -role agent

bootstrap_key=$(sqlite3 $basedir/authority1/certdb.sqlite \
	"select base64(bootstrap_key) from bootstrap where
	subject like '/O=Example/CN=$(hostname)/%';")

# client3 will be used to test the full bootstrap process, everything
# is generated by certalator; REQ, key and cert bootstrap.
mkdir -p $basedir/client3
cp -R $basedir/authority1/crl_store $basedir/client3/
cp $basedir/ca/root.pem $basedir/client3/

cat << EOF > $basedir/client3/certalator.conf
enable_coredumps = true
bootstrap_key = "$bootstrap_key"
authority_fqdn = "localhost"
authority_port = 9791
ca_file = "$basedir/client3/root.pem"
crl_path = "$basedir/client3/crl_store"
cert_file = "$basedir/client3/cert.pem"
key_file = "$basedir/client3/key.pem"
key_bits = 4096
lock_file = "$basedir/client3/coordinator.lock"
coordinator_socket_path = "$basedir/client3/coordinator.sock"
EOF

./certalator -config $basedir/client3/certalator.conf init

cat << EOF > $basedir/client3/mdrd.conf
uid = $uid
gid = $uid
enable_coredumps = true
ca_file = "$basedir/client3/root.pem"
crl_path = "$basedir/client3/crl_store"
cert_file = "$basedir/client3/cert.pem"
key_file = "$basedir/client3/key.pem"
require_client_cert = true

port = 9790
listen_backlog = 1024
prefork = 4
pid_file = "$basedir/client3/mdrd.pid"
counters_sock = "$basedir/client3/counters.sock"

max_clients = 10000
max_conn_per_ip = 25
use_rcv_lowat = true
so_debug = false
rcvbuf = 0
sndbuf = 0
socket_timeout_min = 3
socket_timeout_max = 300
max_payload_size = 16384
max_cert_size = 4096
dbg_delay_to_backend_seconds = 5

allowed_mdr_domains = [ 0x00000002 ]

backend_argv = [
	"$(realpath certalator)"
	"-config"
	"$basedir/client3/certalator.conf"
	"mdrd-backend"
]
backend_uid = $uid
backend_gid = $uid
backend_unveils = [
	"x=$(realpath certalator)"
	"r=$basedir/client3"
	"rw=$basedir/client3/counters.sock"
	"rw=$basedir/client3/mdrd.pid"
	"rw=$basedir/client3/coordinator.lock"
	"rw=$basedir/client3/coordinator.sock"
]
EOF

../mdr/mdrd -c $basedir/client3/mdrd.conf

read -p "Press any key to terminate daemon..." PAUSE
